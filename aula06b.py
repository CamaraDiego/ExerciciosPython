n = input('Digite um valor: ')
print(n.isupper())      #verifica se está tudo em caixa baixa
print(n.islower())      #verifica se está tudo em caixa alta
print(n.isnumeric())    #verifica se tudo é numérico
print(n.isalpha())      #verifica se tudo é alfabeto
print(n.isalnum())      #verifica se tudo é alfanumérico
